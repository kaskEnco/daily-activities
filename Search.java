package Solr.SolrSp;

public class Search {
	private int movie_id;
	private String lyric_title;
	private String movie_name;
	private String lyric_content;
	private String writer_name;

	public int getMovie_id() {
		return movie_id;
	}

	public void setMovie_id(int movie_id) {
		this.movie_id = movie_id;
	}

	public String getLyric_title() {
		return lyric_title;
	}

	public void setLyric_title(String lyric_title) {
		this.lyric_title = lyric_title;
	}

	public String getMovie_name() {
		return movie_name;
	}

	public void setMovie_name(String movie_name) {
		this.movie_name = movie_name;
	}

	public String getLyric_content() {
		return lyric_content;
	}

	public void setLyric_content(String lyric_content) {
		this.lyric_content = lyric_content;
	}

	public String getWriter_name() {
		return writer_name;
	}

	public void setWriter_name(String writer_name) {
		this.writer_name = writer_name;
	}

}
